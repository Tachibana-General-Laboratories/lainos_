int
sys_fork(void)
{
  return fork();
}

int
sys_exit(void)
{
  exit();
  return 0;  // not reached
}

int
sys_wait(void)
{
  return wait();
}

int
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

int
sys_getpid(void)
{
  return proc->pid;
}

int
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = proc->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

int
sys_sleep(void)
{
  int n;
  uint ticks0;
  
  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(proc->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

// return how many clock tick interrupts have occurred
// since start.
int
sys_uptime(void)
{
  uint xticks;
  
  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}

extern void mappages(pde_t *pgdir, void *va, uint size, uint pa, int perm);

// arrange for this process to have a shared memory page
// that will be shared with all children
int
sys_shared(void)
{
  struct shared *sh;

  // if there's already a shared page, return now
  if (proc->shared) {
    return SHARED_V;
  }

  sh = sharedalloc();
  if (sh) {
    proc->shared = sh;
    mappages(proc->pgdir, (char *)SHARED_V, PGSIZE, v2p(sh->page), PTE_W|PTE_U);
    return SHARED_V;
  } else {
    return 0;
  }
}
